// Задача 52. Задайте двумерный массив из целых чисел. 
// Найдите среднее арифметическое элементов в каждом столбце.

// Например, задан массив:
// 1 4 7 2
// 5 9 2 3
// 8 4 2 4
// Среднее арифметическое каждого столбца: 4,6; 5,6; 3,6; 3.

int[,] GetArray(int m, int n)
{
    int[,] result = new int[m, n];

    for (int i = 0; i < m; i++)
    {
        for (int j = 0; j < n; j++)
        {
            result[i, j] = new Random().Next(0, 11);
        }
    }
    return result;
}

void PrintArray(int[,] array)
{
    for (int i = 0; i < array.GetLength(0); i++)
    {
        for (int j = 0; j < array.GetLength(1); j++)
        {
            Console.Write($"{array[i, j]}\t ");
        }
        Console.WriteLine();
    }
}

void AverageArithmeticRowsArray(int[,] array)
{
    for (int j = 0; j < array.GetLength(1); j++)
    {
        double sum = 0;
        for (int i = 0; i < array.GetLength(0); i++)
        {
            sum += array[i, j];
        }
        Console.WriteLine($"Arithmetic mean of the elements of {j} column of the array: {sum / array.GetLength(0)};");
    }
}

Console.Write("Enter the number of rows (at least two): ");
int rows = Convert.ToInt32(Console.ReadLine());
Console.Write("Enter the number of columns (at least two): ");
int columns = Convert.ToInt32(Console.ReadLine());

int[,] myArray = GetArray(rows, columns);
PrintArray(myArray);

AverageArithmeticRowsArray(myArray);